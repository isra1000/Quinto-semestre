package BuilderComputadora;

/**
 *
 * @author Israel
 */
public class Computadora {
    private DiscoDuro discoduro;
    private MemoriaRam memoriaram;
    private Monitor Monitor;
    private Procesador procesador;
    private Teclado teclado;

    /**
     * @return the discoduro
     */
    public DiscoDuro getDiscoduro() {
        return discoduro;
    }

    /**
     * @param discoduro the discoduro to set
     */
    public void setDiscoduro(DiscoDuro discoduro) {
        this.discoduro = discoduro;
    }

    /**
     * @return the memoriaram
     */
    public MemoriaRam getMemoriaram() {
        return memoriaram;
    }

    /**
     * @param memoriaram the memoriaram to set
     */
    public void setMemoriaram(MemoriaRam memoriaram) {
        this.memoriaram = memoriaram;
    }

    /**
     * @return the Monitor
     */
    public Monitor getMonitor() {
        return Monitor;
    }

    /**
     * @param Monitor the Monitor to set
     */
    public void setMonitor(Monitor Monitor) {
        this.Monitor = Monitor;
    }

    /**
     * @return the procesador
     */
    public Procesador getProcesador() {
        return procesador;
    }

    /**
     * @param procesador the procesador to set
     */
    public void setProcesador(Procesador procesador) {
        this.procesador = procesador;
    }

    /**
     * @return the teclado
     */
    public Teclado getTeclado() {
        return teclado;
    }

    /**
     * @param teclado the teclado to set
     */
    public void setTeclado(Teclado teclado) {
        this.teclado = teclado;
    }

    /**
     * @return the discoduro
     */
}